# Multiplies 5 by 2
print(5 * 2)  # Output: 10

# Divides 5 by 2 (returns a float)
print(5 / 2)  # Output: 2.5

# Performs integer division (quotient without remainder)
print(5 // 2)  # Output: 2

# Modulus operation (remainder of division)
print(5 % 2)  # Output: 1

# Raises 5 to the power of 2 (5^2)
print(5 ** 2)  # Output: 25

# Adds 5 and 2
print(5 + 2)  # Output: 7

# Subtracts 2 from 5
print(5 - 2)  # Output: 3
